
public class Variables {

	public static void main(String[] args) {
		
		int A; //defines integer A
		int B = 5; //gives value to integer B while defining it, the value given is 5
		int C, D; //defines integer C and D
		A = 4; //gives a value to integer A of 4
		C = A + B; //gives the value of C as the combined total of integers A and B
		D = 0; //gives a value of 0 to integer D
		D = D + 1; //Adds 1 to the previously defined value of D
		
		System.out.println(A);
		System.out.println(B);
		System.out.println(C);
		System.out.println(D);
		//simple print out of a values of the integers
	}

}